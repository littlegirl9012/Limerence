import UIKit

extension StringProtocol where Index == String.Index {
    func index<T: StringProtocol>(of string: T, options: String.CompareOptions = []) -> Index? {
        return range(of: string, options: options)?.lowerBound
    }
    func endIndex<T: StringProtocol>(of string: T, options: String.CompareOptions = []) -> Index? {
        return range(of: string, options: options)?.upperBound
    }
    func indexes<T: StringProtocol>(of string: T, options: String.CompareOptions = []) -> [Index] {
        var result: [Index] = []
        var start = startIndex
        while start < endIndex, let range = range(of: string, options: options, range: start..<endIndex) {
            result.append(range.lowerBound)
            start = range.lowerBound < range.upperBound ? range.upperBound : index(range.lowerBound, offsetBy: 1, limitedBy: endIndex) ?? endIndex
        }
        return result
    }
    func ranges<T: StringProtocol>(of string: T, options: String.CompareOptions = []) -> [Range<Index>] {
        var result: [Range<Index>] = []
        var start = startIndex
        while start < endIndex, let range = range(of: string, options: options, range: start..<endIndex) {
            result.append(range)
            start = range.lowerBound < range.upperBound  ? range.upperBound : index(range.lowerBound, offsetBy: 1, limitedBy: endIndex) ?? endIndex
        }
        return result
    }
}
public extension String
{
    
    
    

    
    
    static func random(_ length: Int) -> String {
        let allowedChars = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
        let allowedCharsCount = UInt32(allowedChars.characters.count)
        var randomString = ""
        
        for _ in 0..<length {
            let randomNum = Int(arc4random_uniform(allowedCharsCount))
            let randomIndex = allowedChars.index(allowedChars.startIndex, offsetBy: randomNum)
            let newCharacter = allowedChars[randomIndex]
            randomString += String(newCharacter)
        }
        return randomString
    }

    
    func trim() -> String
    {
        let removeSpace  = trimmingCharacters(in: NSCharacterSet.whitespacesAndNewlines)
        return removeSpace
    }

    public func hexColor() -> UIColor {
        let hexint = Int(intFromHexString(self))
        let red = CGFloat((hexint & 0xff0000) >> 16) / 255.0
        let green = CGFloat((hexint & 0xff00) >> 8) / 255.0
        let blue = CGFloat((hexint & 0xff) >> 0) / 255.0
        let color = UIColor(red: red, green: green, blue: blue, alpha: 1)
        return color
    }
    
    fileprivate func intFromHexString(_ hexStr: String) -> UInt32 {
        var hexInt: UInt32 = 0
        let scanner: Scanner = Scanner(string: hexStr)
        scanner.charactersToBeSkipped = CharacterSet(charactersIn: "#")
        scanner.scanHexInt32(&hexInt)
        return hexInt
    }
    
    
    public func image() -> UIImage
    {
        if(self.length == 0)
        {
        }
        
        if(UIImage.init(named: self) != nil)
        {
            return UIImage.init(named: self)!
        }
        return UIImage.init()
    }
    
    public func imageTint(_ color : UIColor) -> UIImage
    {
        return image().tint(color)
    }
    
    
    public func translate() -> String
    {
        
        return NSLocalizedString(self, tableName: nil, bundle: Bundle.main, value: "", comment: "")
    }
    
    
    mutating func appendPrefix(_ value : String)
    {
        self = self.replacingOccurrences(of: "value", with: "")
        self = value.appending(self);
    }
    
    
    public func dataValue() -> Data
    {
        return self.data(using:String.Encoding.utf8, allowLossyConversion: true)!
    }
    
    public func doubleValue() -> Double
    {
        let numberFormater = NumberFormatter()
        numberFormater.groupingSeparator = "."
        let numberValue = numberFormater.number(from: self)?.doubleValue

        if(numberValue != nil)
        {
            return numberValue!
        }
        return 0.0
    }

    public func acsiiDataValue() -> Data
    {
        var searchText = self.lowercased()
        searchText = searchText.replacingOccurrences(of: "đ", with: "d")
        return searchText.data(using:String.Encoding.ascii, allowLossyConversion: true)!
    }
    
    public func lossyValue() -> String
    {
        

        return (NSString(data: acsiiDataValue(), encoding:String.Encoding.utf8.rawValue)! as String).lowercased()
    }
    
    
    public var length: Int
    {
        return self.characters.count
    }
    
    
    public func intValue()-> Int
    {
        if(self.length == 0 || self == "")
        {
            return 0
        }
        
        return Int(self)!
    }
    
    
    public func toBool() -> Bool?
    {
        switch self
        {
        case "True", "true", "yes", "1":
            return true
        case "False", "false", "no", "0","2":
            return false
        default:
            return false
        }
    }
    
    func mineType()->String
    {
        if mimeTypes.contains(where: { $0.0 == self.lowercased() }){
            return mimeTypes[self.lowercased()]!
        }
        return""
    }

    func date8601() -> Date
    {
        
        let dateTime = Formatter.iso8601.date(from: self)
        if(dateTime != nil)
        {
            return dateTime!
        }
        else
        {
            return Date()
        }
    }
    func randomStringWithLength (len : Int) -> NSString {
        
        let letters : NSString = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
        
        var randomString : NSMutableString = NSMutableString(capacity: len)
        
        for  i in 0...len{
            var length = UInt32 (letters.length)
            var rand = arc4random_uniform(length)
            randomString.appendFormat("%C", letters.character(at: Int(rand)))
        }
        
        return randomString
    }

    func capitalizingFirstLetter() -> String {
        return prefix(1).capitalized + dropFirst()
    }
    
    mutating func capitalizeFirstLetter() {
        self = self.capitalizingFirstLetter()
    }

    
}



